package basicJava;

public class Operators {
	public static void main(String[] args) {

		int a = 10;
		int b = 5;
		int c = 20;

		// Arithmetic operators
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a % b);
		
		// Conditional or Rational operators & Logical operators
		
		
		System.out.println(a<b && a<c);
		System.out.println(a>b || a<c);
		
		
		

	}
}
